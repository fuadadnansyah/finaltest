# Array 1 Dimensi (1D)
array_1d = [10, 20, 30, 40, 50]

# Array 2 Dimensi (2D)
array_2d = [
    [1, 2, 3],
    [4, 5, 6]
]

# Array 3 Dimensi (3D)
array_3d = [
    [
        [1, 2],
        [3, 4]
    ],
    [
        [5, 6],
        [7, 8]
    ]
]

print("Array 1 Dimensi (1D):", array_1d)
print("Array 2 Dimensi (2D):", array_2d)
print("Array 3 Dimensi (3D):", array_3d)
